////////////////////////////////////////////////////////////////////////////////////////////////////
//
// DIP_Filter
//
// Class used to manage various types of filters that are applied to a neighbourhood of pixels.
// Note that all neighbourhood based filters are to derive from this class.
//
////////////////////////////////////////////////////////////////////////////////////////////////////



#include "DIP_Filter.h"



////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Default Constructor
//
////////////////////////////////////////////////////////////////////////////////////////////////////
DIP_Filter::DIP_Filter ( DIP_Neighbourhood * iNeighbourhood )
{
    _pNeighbourhood = iNeighbourhood;
}



////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Destructor
//
////////////////////////////////////////////////////////////////////////////////////////////////////
DIP_Filter::~DIP_Filter ()
{
    _pNeighbourhood = NULL;
}



